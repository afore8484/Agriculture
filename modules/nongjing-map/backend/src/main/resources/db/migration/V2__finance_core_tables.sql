CREATE TABLE IF NOT EXISTS dim_org (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    org_code VARCHAR(32) NOT NULL UNIQUE,
    org_name VARCHAR(128) NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    org_level VARCHAR(32) NOT NULL DEFAULT 'village',
    parent_org_code VARCHAR(32),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_dim_org_region_code ON dim_org(region_code);

CREATE TABLE IF NOT EXISTS dim_income_band (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    band_code VARCHAR(20) NOT NULL UNIQUE,
    band_name VARCHAR(64) NOT NULL,
    threshold_amount NUMERIC(18,2) NOT NULL,
    sort_no INT NOT NULL DEFAULT 0,
    remark TEXT,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_dim_income_band_sort_no ON dim_income_band(sort_no);

CREATE TABLE IF NOT EXISTS fact_finance_monthly_summary (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_month VARCHAR(6) NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    org_code VARCHAR(32) NOT NULL,
    total_income NUMERIC(18,2) NOT NULL DEFAULT 0,
    operating_income NUMERIC(18,2) NOT NULL DEFAULT 0,
    subsidy_income NUMERIC(18,2) NOT NULL DEFAULT 0,
    investment_income NUMERIC(18,2) NOT NULL DEFAULT 0,
    other_income NUMERIC(18,2) NOT NULL DEFAULT 0,
    total_expense NUMERIC(18,2) NOT NULL DEFAULT 0,
    operating_expense NUMERIC(18,2) NOT NULL DEFAULT 0,
    management_expense NUMERIC(18,2) NOT NULL DEFAULT 0,
    year_profit NUMERIC(18,2) NOT NULL DEFAULT 0,
    undistributed_profit NUMERIC(18,2) NOT NULL DEFAULT 0,
    currency_funds NUMERIC(18,2) NOT NULL DEFAULT 0,
    accounts_receivable NUMERIC(18,2) NOT NULL DEFAULT 0,
    inventory_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    fixed_assets_original NUMERIC(18,2) NOT NULL DEFAULT 0,
    fixed_assets_net NUMERIC(18,2) NOT NULL DEFAULT 0,
    accounts_payable NUMERIC(18,2) NOT NULL DEFAULT 0,
    is_closed CHAR(1) NOT NULL DEFAULT 'Y',
    is_account_set_created CHAR(1) NOT NULL DEFAULT 'Y',
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_fact_finance_monthly_summary UNIQUE (period_month, org_code)
);
CREATE INDEX IF NOT EXISTS idx_fact_finance_monthly_summary_region_month ON fact_finance_monthly_summary(region_code, period_month);

CREATE TABLE IF NOT EXISTS fact_finance_statement_line (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    statement_type VARCHAR(30) NOT NULL,
    period_month VARCHAR(6) NOT NULL,
    snapshot_date DATE NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    org_code VARCHAR(32),
    line_code VARCHAR(50) NOT NULL,
    line_name VARCHAR(200) NOT NULL,
    line_no INT NOT NULL,
    parent_line_code VARCHAR(50),
    line_level INT NOT NULL DEFAULT 1,
    line_category VARCHAR(50) NOT NULL,
    begin_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    current_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    end_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    change_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    change_rate NUMERIC(10,4) NOT NULL DEFAULT 0,
    remark VARCHAR(500),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_fact_finance_statement_line_main ON fact_finance_statement_line(region_code, period_month, statement_type);

CREATE TABLE IF NOT EXISTS fact_finance_income_detail (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_month VARCHAR(6) NOT NULL,
    biz_date DATE NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    org_code VARCHAR(32) NOT NULL,
    income_type VARCHAR(50) NOT NULL,
    income_item_code VARCHAR(50),
    income_item_name VARCHAR(200) NOT NULL,
    counterparty_name VARCHAR(200),
    amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    source_doc_no VARCHAR(100),
    source_system VARCHAR(50),
    remark VARCHAR(500),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_fact_finance_income_detail_main ON fact_finance_income_detail(region_code, period_month, income_type);

CREATE TABLE IF NOT EXISTS fact_finance_receivable_detail (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_month VARCHAR(6) NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    org_code VARCHAR(32) NOT NULL,
    debtor_name VARCHAR(200) NOT NULL,
    contract_no VARCHAR(100),
    amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    received_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    unreceived_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    due_date DATE,
    aging_days INT NOT NULL DEFAULT 0,
    status VARCHAR(32) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_fact_finance_receivable_detail_main ON fact_finance_receivable_detail(region_code, period_month, status);

CREATE TABLE IF NOT EXISTS fact_finance_payable_detail (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_month VARCHAR(6) NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    org_code VARCHAR(32) NOT NULL,
    creditor_name VARCHAR(200) NOT NULL,
    contract_no VARCHAR(100),
    amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    paid_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    unpaid_amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    due_date DATE,
    aging_days INT NOT NULL DEFAULT 0,
    status VARCHAR(32) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_fact_finance_payable_detail_main ON fact_finance_payable_detail(region_code, period_month, status);

CREATE TABLE IF NOT EXISTS fact_finance_income_distribution_line (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_month VARCHAR(6) NOT NULL,
    snapshot_date DATE NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    org_code VARCHAR(32),
    item_code VARCHAR(50) NOT NULL,
    item_name VARCHAR(200) NOT NULL,
    line_no INT NOT NULL,
    amount NUMERIC(18,2) NOT NULL DEFAULT 0,
    remark VARCHAR(500),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_fact_finance_income_distribution_line_main ON fact_finance_income_distribution_line(region_code, period_month);

CREATE TABLE IF NOT EXISTS mv_finance_income_band_monthly (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_month VARCHAR(6) NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    band_code VARCHAR(20) NOT NULL,
    band_name VARCHAR(50) NOT NULL,
    village_count INT NOT NULL DEFAULT 0,
    income_total NUMERIC(18,2) NOT NULL DEFAULT 0,
    village_ratio NUMERIC(10,4) NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_mv_finance_income_band_monthly UNIQUE (period_month, region_code, band_code)
);
CREATE INDEX IF NOT EXISTS idx_mv_finance_income_band_monthly_main ON mv_finance_income_band_monthly(region_code, period_month);

