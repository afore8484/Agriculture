CREATE SCHEMA IF NOT EXISTS nongjing_map;
SET search_path TO nongjing_map, public;

CREATE TABLE IF NOT EXISTS dim_region (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    region_code VARCHAR(32) NOT NULL UNIQUE,
    region_name VARCHAR(128) NOT NULL,
    parent_code VARCHAR(32),
    region_level VARCHAR(32) NOT NULL,
    sort_no INT NOT NULL DEFAULT 0,
    geom_wkt TEXT,
    centroid_wkt TEXT,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_dim_region_parent_code ON dim_region(parent_code);
CREATE INDEX IF NOT EXISTS idx_dim_region_level ON dim_region(region_level);

CREATE TABLE IF NOT EXISTS dim_time (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    time_code VARCHAR(16) NOT NULL UNIQUE,
    date_value DATE NOT NULL,
    year_no INT NOT NULL,
    quarter_no INT NOT NULL,
    month_no INT NOT NULL,
    month_label VARCHAR(16) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_dim_time_ym ON dim_time(year_no, month_no);

CREATE TABLE IF NOT EXISTS dim_metric (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    metric_code VARCHAR(64) NOT NULL UNIQUE,
    metric_name VARCHAR(128) NOT NULL,
    metric_category VARCHAR(64) NOT NULL,
    unit_name VARCHAR(32),
    remark TEXT,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_dim_metric_category ON dim_metric(metric_category);

CREATE TABLE IF NOT EXISTS dim_dict_item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dict_type VARCHAR(64) NOT NULL,
    item_code VARCHAR(64) NOT NULL,
    item_name VARCHAR(128) NOT NULL,
    sort_no INT NOT NULL DEFAULT 0,
    remark TEXT,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_dim_dict_item UNIQUE (dict_type, item_code)
);
CREATE INDEX IF NOT EXISTS idx_dim_dict_item_type ON dim_dict_item(dict_type);

CREATE TABLE IF NOT EXISTS fact_weather_daily (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    region_code VARCHAR(32) NOT NULL,
    weather_date DATE NOT NULL,
    weather_type VARCHAR(64) NOT NULL,
    temperature_text VARCHAR(32) NOT NULL,
    wind_text VARCHAR(64),
    humidity_text VARCHAR(32),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_fact_weather_daily UNIQUE (region_code, weather_date)
);
CREATE INDEX IF NOT EXISTS idx_fact_weather_daily_region_date ON fact_weather_daily(region_code, weather_date);

CREATE TABLE IF NOT EXISTS mv_finance_overview_monthly (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    region_code VARCHAR(32) NOT NULL,
    stat_month VARCHAR(6) NOT NULL,
    org_count INT NOT NULL DEFAULT 0,
    township_count INT NOT NULL DEFAULT 0,
    village_count INT NOT NULL DEFAULT 0,
    unsettled_count INT NOT NULL DEFAULT 0,
    no_book_count INT NOT NULL DEFAULT 0,
    warning_count INT NOT NULL DEFAULT 0,
    income_total NUMERIC(18,2) NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_mv_finance_overview_monthly UNIQUE (region_code, stat_month)
);
CREATE INDEX IF NOT EXISTS idx_mv_finance_overview_monthly_region_month ON mv_finance_overview_monthly(region_code, stat_month);

CREATE TABLE IF NOT EXISTS mv_region_topic_rank_monthly (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    metric_code VARCHAR(64) NOT NULL,
    topic_code VARCHAR(64) NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    region_name VARCHAR(128) NOT NULL,
    stat_month VARCHAR(6) NOT NULL,
    rank_no INT NOT NULL,
    metric_value NUMERIC(18,2) NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_mv_region_topic_rank_monthly_metric ON mv_region_topic_rank_monthly(metric_code, stat_month);

