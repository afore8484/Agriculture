CREATE TABLE IF NOT EXISTS fact_resource_item (
    resource_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    region_code VARCHAR(32) NOT NULL,
    org_code VARCHAR(32) NOT NULL,
    resource_code VARCHAR(100) NOT NULL UNIQUE,
    resource_name VARCHAR(200) NOT NULL,
    resource_type VARCHAR(50) NOT NULL,
    resource_subtype VARCHAR(100),
    ownership_type VARCHAR(50),
    location_desc VARCHAR(300),
    area NUMERIC(18,4) NOT NULL DEFAULT 0,
    quantity NUMERIC(18,4) NOT NULL DEFAULT 0,
    unit VARCHAR(20),
    estimated_value NUMERIC(18,2) NOT NULL DEFAULT 0,
    usable_status VARCHAR(50) NOT NULL,
    period_month VARCHAR(6) NOT NULL,
    remark VARCHAR(500),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_fact_resource_item_region_month ON fact_resource_item(region_code, period_month);
CREATE INDEX IF NOT EXISTS idx_fact_resource_item_type ON fact_resource_item(resource_type);

CREATE TABLE IF NOT EXISTS fact_resource_summary_monthly (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_month VARCHAR(6) NOT NULL,
    region_code VARCHAR(32) NOT NULL,
    org_code VARCHAR(32),
    resource_type VARCHAR(50) NOT NULL,
    resource_total_area NUMERIC(18,4) NOT NULL DEFAULT 0,
    resource_total_quantity NUMERIC(18,4) NOT NULL DEFAULT 0,
    resource_total_value NUMERIC(18,2) NOT NULL DEFAULT 0,
    resource_item_count INT NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_fact_resource_summary_monthly UNIQUE (period_month, region_code, org_code, resource_type)
);
CREATE INDEX IF NOT EXISTS idx_fact_resource_summary_monthly_region_month ON fact_resource_summary_monthly(region_code, period_month);

CREATE TABLE IF NOT EXISTS resource_fetch_job (
    job_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_name VARCHAR(200) NOT NULL,
    source_system VARCHAR(100) NOT NULL,
    scope_region_code VARCHAR(32),
    scope_desc VARCHAR(200),
    status VARCHAR(32) NOT NULL,
    last_run_time TIMESTAMP,
    period_month VARCHAR(6),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_resource_fetch_job_status ON resource_fetch_job(status);

