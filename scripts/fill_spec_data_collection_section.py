def insert_paragraph_after(anchor, text=""):
    new_p = OxmlElement("w:p")
    anchor._p.addnext(new_p)
    para = Paragraph(new_p, anchor._parent)
    if text:
        para.add_run(text)
    return para


def insert_4col_table_after(doc, anchor, data):
    sample = doc.tables[2]
    new_tbl = deepcopy(sample._tbl)
    for tr in list(new_tbl.tr_lst):
        new_tbl.remove(tr)
    row_template = deepcopy(sample.rows[0]._tr)
    for _ in data:
        new_tbl.append(deepcopy(row_template))
    anchor._p.addnext(new_tbl)
    table = Table(new_tbl, anchor._parent)
    table.style = sample.style
    for r_idx, row in enumerate(data):
        for c_idx, value in enumerate(row[:4]):
            table.cell(r_idx, c_idx).text = str(value)
    return table


def delete_between(p_start, p_end):
    cur = p_start._p.getnext()
    while cur is not None and cur != p_end._p:
        nxt = cur.getnext()
        cur.getparent().remove(cur)
        cur = nxt


def load_scoped_data(category_values):
    data = json.loads(SCOPED_JSON_PATH.read_text(encoding="utf-8"))
    records = data["records"]
    keys = list(records[0].keys())
    category_key = keys[5]
    ip_key = next(k for k in keys if "IP" in k)
    must = [r for r in records if r[category_key] == category_values["must"]]
    reserve = [r for r in records if r[category_key] == category_values["reserve"]]
    no = [r for r in records if r[category_key] == category_values["no"]]
    ip_counter = Counter(r[ip_key] for r in must)
    return records, must, reserve, no, ip_counter


