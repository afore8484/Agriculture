def set_cn_font(run, size=12, bold=False):
    run.font.name = "宋体"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
    run.font.size = Pt(size)
    run.bold = bold


def add_run(para, text, size=12, bold=False, center=False):
    run = para.add_run(text)
    set_cn_font(run, size=size, bold=bold)
    if center:
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    return run


def add_page_number(section):
    footer = section.footer
    para = footer.paragraphs[0]
    para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = para.add_run()
    fld_begin = OxmlElement("w:fldChar")
    fld_begin.set(qn("w:fldCharType"), "begin")
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = " PAGE "
    fld_end = OxmlElement("w:fldChar")
    fld_end.set(qn("w:fldCharType"), "end")
    run._r.append(fld_begin)
    run._r.append(instr)
    run._r.append(fld_end)
    set_cn_font(run, 10)


def heading(doc, text, level=1):
    para = doc.add_paragraph()
    size_map = {1: 16, 2: 14, 3: 12}
    add_run(para, text, size=size_map.get(level, 12), bold=True)
    return para


def paragraph(doc, text):
    para = doc.add_paragraph()
    para.paragraph_format.first_line_indent = Cm(0.74)
    para.paragraph_format.line_spacing = 1.5
    add_run(para, text, size=12)
    return para


def numbered(doc, items):
    for item in items:
        para = doc.add_paragraph(style="List Number")
        para.paragraph_format.line_spacing = 1.5
        add_run(para, item, size=12)


